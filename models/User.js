import mongoose from "mongoose";

/* Create a Schema for the user login and register */
const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// Export the userSchema variable
export const User = mongoose.model("User", userSchema);
